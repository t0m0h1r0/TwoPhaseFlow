"""
Stationary Droplet Benchmark (Benchmark 1).

Implements §10.3 (§\ref{sec:bench_droplet}) of the paper.

Purpose: Verify the CSF surface-tension implementation and quantify
parasitic (spurious) currents generated by the CCD curvature scheme.

Setup:
    Square domain [0, 1]², center (0.5, 0.5), radius R = 0.25 droplet.
    ρ_l / ρ_g = 1000 (rho_ratio = 0.001), We = 1, Fr → ∞ (no gravity).
    Initial velocity u = 0.

Expected results:
    1. Droplet remains stationary: ||u||_∞ ≈ 0 (ideal).
    2. Laplace pressure jump Δp = p_in − p_out ≈ σ / R = (1/We) / R = 4.0.
    3. Normalized parasitic current ||u||_∞ / (σ/μ_l) ≤ O(10⁻⁵) at N = 64.
       (σ/μ_l = Re/We in the dimensionless formulation used here.)
    4. Parasitic currents converge as O(h^p), p ≥ 4, with grid refinement.

Usage::

    from twophase.benchmarks import StationaryDropletBenchmark

    bench = StationaryDropletBenchmark(N=64, t_end=0.1)
    results = bench.run(verbose=True)
    bench.print_metrics(results)
    bench.plot(results, save_dir="results/stationary_droplet/")

Grid convergence study::

    bench = StationaryDropletBenchmark()
    conv = bench.run_convergence(N_list=[32, 64, 128])
    bench.print_convergence(conv)
"""

from __future__ import annotations
import numpy as np
from typing import Dict, List, Optional, Any
from ..initial_conditions import InitialConditionBuilder, Circle


class StationaryDropletBenchmark:
    """Stationary droplet benchmark (§10.3, Benchmark 1).

    Parameters
    ----------
    N       : grid points per side (square domain)
    t_end   : simulation end time (paper: t = 0.1)
    Re      : Reynolds number (controls μ_l = 1/Re; affects parasitic-current
               normalization; no direct physical meaning for zero-velocity IC)
    verbose : print progress
    """

    # Benchmark geometry (dimensionless)
    RADIUS: float = 0.25
    CENTER: tuple = (0.5, 0.5)

    # Fluid parameters (paper §10.3)
    WE: float = 1.0          # Weber number
    RHO_RATIO: float = 0.001  # ρ_g / ρ_l = 1/1000
    FR: float = 1.0e10       # effectively ∞ (no gravity)

    # Theoretical Laplace pressure jump: σ/R with σ = 1/We
    # In dimensionless form: Δp_theory = (1/We) / R
    LAPLACE_PRESSURE_REF: float = 1.0 / (WE * RADIUS)  # = 4.0

    def __init__(
        self,
        N: int = 64,
        t_end: float = 0.1,
        Re: float = 100.0,
        verbose: bool = True,
    ):
        self.N = N
        self.t_end = t_end
        self.Re = Re
        self.verbose = verbose

    # ── Configuration ────────────────────────────────────────────────────

    def _make_config(self, N: int):
        """Build SimulationConfig for the given resolution."""
        from ..config import (
            SimulationConfig, GridConfig, FluidConfig,
            NumericsConfig, SolverConfig,
        )
        return SimulationConfig(
            grid=GridConfig(ndim=2, N=(N, N), L=(1.0, 1.0)),
            fluid=FluidConfig(
                Re=self.Re,
                Fr=self.FR,
                We=self.WE,
                rho_ratio=self.RHO_RATIO,
                mu_ratio=self.RHO_RATIO,  # μ_ratio ≈ ρ_ratio for same kinematic viscosity
            ),
            numerics=NumericsConfig(
                epsilon_factor=1.5,
                reinit_steps=4,
                cfl_number=0.25,
                t_end=self.t_end,
                cn_viscous=True,
                bc_type="wall",
            ),
            solver=SolverConfig(
                ppe_solver_type="ccd_lu",
            ),
        )

    # ── Single-resolution run ────────────────────────────────────────────

    def run(self, verbose: Optional[bool] = None) -> Dict[str, Any]:
        """Run the benchmark and return measurement results.

        Returns
        -------
        dict with keys:
            times              : array of recorded times
            max_velocity       : array of ||u||_∞ at each recorded time
            laplace_pressure   : Δp at final time (p_inside − p_outside)
            laplace_error      : |Δp − Δp_theory| / Δp_theory (relative)
            parasitic_norm     : ||u||_∞ / (σ/μ_l) at t = t_end
            passed             : bool — parasitic_norm ≤ 1e-4 (practical threshold)
            sim                : TwoPhaseSimulation instance (for post-processing)
        """
        if verbose is None:
            verbose = self.verbose

        from ..simulation.builder import SimulationBuilder

        cfg = self._make_config(self.N)
        sim = SimulationBuilder(cfg).build()
        psi_np = (
            InitialConditionBuilder(background_phase="gas")
            .add(Circle(center=self.CENTER, radius=self.RADIUS))
            .build(sim.grid, sim.eps)
        )
        sim.psi.data = sim.backend.to_device(psi_np)

        times: List[float] = []
        max_vel: List[float] = []

        def record(s):
            xp = s.backend.xp
            vel_comps = [s.velocity[ax] for ax in range(s.config.grid.ndim)]
            u_max = max(float(xp.max(xp.abs(v))) for v in vel_comps)
            times.append(s.time)
            max_vel.append(u_max)

        record(sim)  # record initial state

        output_interval = max(1, int(self.t_end / 0.01 / 10))
        sim.run(
            t_end=self.t_end,
            output_interval=output_interval,
            verbose=verbose,
            callback=record,
        )

        # Laplace pressure jump at final state
        dp, dp_err = self._measure_laplace_pressure(sim)

        # Normalised parasitic current: ||u||_∞ / (σ/μ_l)
        # In dimensionless units: σ/μ_l = Re/We
        sigma_over_mu = self.Re / self.WE
        parasitic_norm = max_vel[-1] / max(sigma_over_mu, 1e-14)

        return {
            "times":            np.array(times),
            "max_velocity":     np.array(max_vel),
            "laplace_pressure": dp,
            "laplace_error":    dp_err,
            "parasitic_norm":   parasitic_norm,
            "passed":           parasitic_norm <= 1e-4,
            "sim":              sim,
        }

    # ── Grid convergence study ───────────────────────────────────────────

    def run_convergence(
        self,
        N_list: Optional[List[int]] = None,
    ) -> Dict[str, Any]:
        """Run at multiple resolutions to measure parasitic-current convergence.

        Parameters
        ----------
        N_list : list of N values (grid points per side)

        Returns
        -------
        dict with keys:
            N_list          : list of N values
            h_list          : cell sizes 1/N
            parasitic_norms : ||u||_∞/(σ/μ_l) at each N
            orders          : convergence orders between consecutive N values
        """
        if N_list is None:
            N_list = [32, 64, 128]

        parasitic_norms: List[float] = []
        for N in N_list:
            bench = StationaryDropletBenchmark(
                N=N, t_end=self.t_end, Re=self.Re, verbose=False,
            )
            results = bench.run(verbose=False)
            parasitic_norms.append(results["parasitic_norm"])
            if self.verbose:
                print(f"  N={N:4d}: ||u_para||/(σ/μ) = {parasitic_norms[-1]:.3e}")

        h_list = [1.0 / N for N in N_list]
        orders: List[float] = []
        for i in range(1, len(N_list)):
            if parasitic_norms[i] > 0 and parasitic_norms[i - 1] > 0:
                p = np.log2(parasitic_norms[i - 1] / parasitic_norms[i]) / np.log2(
                    h_list[i - 1] / h_list[i]
                )
            else:
                p = float("nan")
            orders.append(p)

        return {
            "N_list":          N_list,
            "h_list":          h_list,
            "parasitic_norms": parasitic_norms,
            "orders":          orders,
        }

    # ── Metrics ──────────────────────────────────────────────────────────

    def _measure_laplace_pressure(self, sim) -> tuple:
        """Compute the Laplace pressure jump and its relative error."""
        xp = sim.backend.xp
        psi = sim.psi.data
        p   = sim.pressure.data

        inside  = psi > 0.5
        outside = psi < 0.5

        n_in  = int(xp.sum(inside))
        n_out = int(xp.sum(outside))
        if n_in == 0 or n_out == 0:
            return 0.0, 1.0

        p_in  = float(xp.sum(p[inside]))  / n_in
        p_out = float(xp.sum(p[outside])) / n_out
        dp = p_in - p_out

        dp_err = abs(dp - self.LAPLACE_PRESSURE_REF) / abs(self.LAPLACE_PRESSURE_REF)
        return dp, dp_err

    def print_metrics(self, results: Dict[str, Any]) -> None:
        """Print benchmark results to stdout."""
        dp       = results["laplace_pressure"]
        dp_err   = results["laplace_error"]
        u_max    = results["max_velocity"][-1]
        par_norm = results["parasitic_norm"]
        sigma_over_mu = self.Re / self.WE

        print("\n=== Stationary Droplet Benchmark Results ===")
        print(f"  N = {self.N},  t_end = {self.t_end},  Re = {self.Re},  We = {self.WE}")
        print(f"  Laplace pressure Δp : {dp:.6f}  "
              f"(theory: {self.LAPLACE_PRESSURE_REF:.4f},  "
              f"relative error: {dp_err:.3e})")
        print(f"  Max velocity ||u||_∞ : {u_max:.3e}")
        print(f"  σ/μ_l (= Re/We)      : {sigma_over_mu:.3e}")
        print(f"  Parasitic norm        : {par_norm:.3e}  "
              f"(threshold ≤ 1e-4 → {'PASS' if par_norm <= 1e-4 else 'FAIL'})")
        print("============================================\n")

    def print_convergence(self, conv: Dict[str, Any]) -> None:
        """Print grid-convergence table."""
        print("\n=== Parasitic Current Grid Convergence ===")
        print(f"  {'N':>6}  {'h':>10}  {'||u_para||/(σ/μ)':>18}  {'order':>6}")
        print("  " + "-" * 50)
        orders = conv["orders"]
        for i, (N, h, pn) in enumerate(
            zip(conv["N_list"], conv["h_list"], conv["parasitic_norms"])
        ):
            order_str = f"{orders[i-1]:.2f}" if i > 0 and i - 1 < len(orders) else "—"
            print(f"  {N:>6}  {h:>10.5f}  {pn:>18.3e}  {order_str:>6}")
        print("==========================================\n")

    # ── Plotting ─────────────────────────────────────────────────────────

    def plot(
        self,
        results: Dict[str, Any],
        save_dir: Optional[str] = None,
    ) -> None:
        """Plot benchmark results.

        Parameters
        ----------
        results  : return value of run()
        save_dir : if given, save PNG files here
        """
        import matplotlib.pyplot as plt
        import os

        if save_dir is not None:
            os.makedirs(save_dir, exist_ok=True)

        t  = results["times"]
        uv = results["max_velocity"]

        # ── 1. Max velocity vs time (parasitic currents) ─────────────────
        fig, ax = plt.subplots(figsize=(7, 4), dpi=100)
        ax.semilogy(t, uv + 1e-20, "b-", label=r"$\|\mathbf{u}\|_\infty$")
        sigma_over_mu = self.Re / self.WE
        threshold = 1e-4 * sigma_over_mu
        ax.axhline(threshold, color="r", linestyle="--",
                   label=r"threshold $10^{-4}\,(\sigma/\mu)$")
        ax.set_xlabel("time $t$")
        ax.set_ylabel(r"$\|\mathbf{u}\|_\infty$")
        ax.set_title("Stationary Droplet: max velocity (parasitic currents)")
        ax.legend()
        ax.grid(True, alpha=0.4)
        if save_dir:
            fig.savefig(os.path.join(save_dir, "max_velocity.png"),
                        bbox_inches="tight")
        plt.show()

        # ── 2. Final ψ field ─────────────────────────────────────────────
        sim = results.get("sim")
        if sim is not None:
            from ..visualization.plot_scalar import plot_level_set
            fig = plot_level_set(
                np.asarray(sim.backend.to_host(sim.psi.data)),
                sim.grid,
                title=f"Stationary Droplet t={sim.time:.3f}: ψ",
            )
            if save_dir:
                fig.savefig(os.path.join(save_dir, "final_psi.png"),
                            bbox_inches="tight")
            plt.show()
