#!/usr/bin/env python3
"""【11-3a】Taylor-Green vortex temporal convergence (full pipeline).

Paper ref: §11.3.1 — NS total time accuracy

Same as exp10_10 but reframed as a system-level verification.
Fix N=64 (spectral-accurate PPE removes spatial error), vary Δt.
Expected: O(Δt²) from AB2+IPC.

Also measures pressure convergence order.
"""

import sys, pathlib
sys.path.insert(0, str(pathlib.Path(__file__).resolve().parents[2] / "src"))

import numpy as np
import time as _time
from twophase.backend import Backend
from twophase.core.grid import Grid
from twophase.config import GridConfig
from twophase.ccd.ccd_solver import CCDSolver

sys.path.insert(0, str(pathlib.Path(__file__).resolve().parent.parent / "ch10"))
from exp10_10_ipc_time_accuracy import (
    tgv_exact, SpectralPPE, SpectralHelmholtz,
    ccd_divergence, ccd_gradient, ccd_laplacian, ccd_convection,
    run_ab2_ipc,
)

OUT = pathlib.Path(__file__).resolve().parent.parent.parent / "results" / "ch11_tgv_temporal"
OUT.mkdir(parents=True, exist_ok=True)


def main():
    print("\n" + "=" * 80)
    print("  【11-3a】NS Time Accuracy: Taylor-Green Vortex (AB2+IPC)")
    print("=" * 80 + "\n")

    N = 64
    Re = 100.0
    nu = 1.0 / Re
    L = 2.0 * np.pi
    T_end = 0.5

    backend = Backend(use_gpu=False)
    gc = GridConfig(ndim=2, N=(N, N), L=(L, L))
    grid = Grid(gc, backend)
    ccd = CCDSolver(grid, backend, bc_type="periodic")
    ppe_solver = SpectralPPE(N, L)

    n_steps_list = [5, 10, 20, 40, 80, 160]
    results = []

    print(f"  N={N}, Re={Re}, T_end={T_end}")
    print(f"  {'n_steps':>8} {'dt':>10} {'L_inf(u)':>12} {'order(u)':>10} {'L_inf(p)':>12} {'order(p)':>10}")
    print("  " + "-" * 75)

    for n_steps in n_steps_list:
        dt = T_end / n_steps

        u, v, p, X, Y = run_ab2_ipc(N, L, nu, dt, n_steps, ccd, backend, ppe_solver, grid)
        u_exact, v_exact, p_exact = tgv_exact(X, Y, T_end, nu)

        err_u = np.max(np.abs(u - u_exact))
        err_v = np.max(np.abs(v - v_exact))
        err_vel = max(err_u, err_v)

        # Pressure gradient error (gauge-independent)
        dp_dx_num, dp_dy_num = ccd_gradient(p, ccd, backend)
        dp_dx_ex, dp_dy_ex = ccd_gradient(p_exact, ccd, backend)
        err_p = max(np.max(np.abs(dp_dx_num - dp_dx_ex)),
                    np.max(np.abs(dp_dy_num - dp_dy_ex)))

        results.append({"n_steps": n_steps, "dt": dt,
                        "err_vel": err_vel, "err_p": err_p})

        order_u_str = "---"
        order_p_str = "---"
        if len(results) > 1:
            r0, r1 = results[-2], results[-1]
            if r0["err_vel"] > 0 and r1["err_vel"] > 0:
                s = np.log(r1["err_vel"] / r0["err_vel"]) / np.log(r1["dt"] / r0["dt"])
                order_u_str = f"{s:.2f}"
            if r0["err_p"] > 0 and r1["err_p"] > 0:
                s = np.log(r1["err_p"] / r0["err_p"]) / np.log(r1["dt"] / r0["dt"])
                order_p_str = f"{s:.2f}"

        print(f"  {n_steps:>8} {dt:>10.5f} {err_vel:>12.3e} {order_u_str:>10} "
              f"{err_p:>12.3e} {order_p_str:>10}")

    # Save LaTeX table
    with open(OUT / "table_tgv_temporal.tex", "w") as fp:
        fp.write("% Auto-generated by exp11_4_tgv_temporal.py\n")
        fp.write("\\begin{tabular}{rrcrcr}\n\\toprule\n")
        fp.write("ステップ数 & $\\Delta t$ & $L^\\infty(\\bu)$ & 次数 & $L^\\infty(p)$ & 次数 \\\\\n")
        fp.write("\\midrule\n")
        for i, r in enumerate(results):
            ou = "---"
            op = "---"
            if i > 0:
                r0 = results[i-1]
                if r0["err_vel"] > 0 and r["err_vel"] > 0:
                    ou = f"${np.log(r['err_vel']/r0['err_vel'])/np.log(r['dt']/r0['dt']):.2f}$"
                if r0["err_p"] > 0 and r["err_p"] > 0:
                    op = f"${np.log(r['err_p']/r0['err_p'])/np.log(r['dt']/r0['dt']):.2f}$"
            fp.write(f"{r['n_steps']} & ${r['dt']:.4f}$ & ${r['err_vel']:.2e}$ & {ou} "
                     f"& ${r['err_p']:.2e}$ & {op} \\\\\n")
        fp.write("\\bottomrule\n\\end{tabular}\n")
    print(f"\n  Saved: {OUT / 'table_tgv_temporal.tex'}")

    # Save plot
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt

        fig, ax = plt.subplots(1, 1, figsize=(7, 5))
        dts = [r["dt"] for r in results]
        errs_u = [r["err_vel"] for r in results]
        errs_p = [r["err_p"] for r in results]

        ax.loglog(dts, errs_u, "o-", label="$L^\\infty$ velocity", markersize=6)
        ax.loglog(dts, errs_p, "s-", label="$L^\\infty$ pressure", markersize=6)

        dt_ref = np.array([dts[0], dts[-1]])
        e0 = errs_u[0]
        ax.loglog(dt_ref, e0 * (dt_ref/dt_ref[0])**2, "--", color="gray", alpha=0.5, label="$O(\\Delta t^2)$")
        ax.loglog(dt_ref, e0 * (dt_ref/dt_ref[0])**1, ":", color="gray", alpha=0.5, label="$O(\\Delta t)$")

        ax.set_xlabel("$\\Delta t$")
        ax.set_ylabel("$L^\\infty$ error")
        ax.set_title(f"NS System Time Accuracy (N={N}, Re={Re})")
        ax.legend()
        ax.grid(True, which="both", alpha=0.3)
        fig.tight_layout()
        fig.savefig(OUT / "tgv_temporal_convergence.png", dpi=150, bbox_inches="tight")
        plt.close(fig)
        print(f"  Saved: {OUT / 'tgv_temporal_convergence.png'}")
    except ImportError:
        pass

    np.savez(OUT / "tgv_temporal_data.npz", results=results)
    print(f"  All results saved to {OUT}")


if __name__ == "__main__":
    main()
