#!/usr/bin/env python3
"""【11-3b】Kovasznay flow spatial accuracy — MMS residual approach.

Paper ref: §11.3.2 — Steady 2D NS analytical solution (Re=40)

Rather than time-marching to steady state, we evaluate the CCD-discretized
NS residual on the exact Kovasznay solution and measure how the residual
decreases with grid refinement.

This tests the spatial accuracy of the assembled CCD operator chain
(convection + viscous + pressure gradient) without time-stepping instability.

Kovasznay (1948) exact solution:
  u = 1 - exp(λx) cos(2πy)
  v = (λ/2π) exp(λx) sin(2πy)
  p = -exp(2λx)/2
  λ = Re/2 - sqrt(Re²/4 + 4π²)
"""

import sys, pathlib
sys.path.insert(0, str(pathlib.Path(__file__).resolve().parents[2] / "src"))

import numpy as np
from twophase.backend import Backend
from twophase.core.grid import Grid
from twophase.config import GridConfig
from twophase.ccd.ccd_solver import CCDSolver

OUT = pathlib.Path(__file__).resolve().parent.parent.parent / "results" / "ch11_kovasznay"
OUT.mkdir(parents=True, exist_ok=True)


def kovasznay_exact(X, Y, Re):
    lam = Re / 2.0 - np.sqrt(Re**2 / 4.0 + 4.0 * np.pi**2)
    u = 1.0 - np.exp(lam * X) * np.cos(2.0 * np.pi * Y)
    v = lam / (2.0 * np.pi) * np.exp(lam * X) * np.sin(2.0 * np.pi * Y)
    p = -0.5 * np.exp(2.0 * lam * X)
    return u, v, p, lam


def ccd_ops(f, ccd, backend, axis):
    xp = backend.xp
    d1, d2 = ccd.differentiate(xp.asarray(f), axis=axis)
    return np.asarray(backend.to_host(d1)), np.asarray(backend.to_host(d2))


def compute_ns_residual(N, Re=40.0):
    """Compute CCD-discretized NS residual on exact Kovasznay solution.

    NS steady state: -u·∇u + ν∇²u - ∇p = 0
    Residual should be O(h^p) where p is the CCD spatial accuracy.
    """
    backend = Backend(use_gpu=False)
    nu = 1.0 / Re
    Lx, Ly = 1.0, 1.0
    Nx, Ny = N, N

    gc = GridConfig(ndim=2, N=(Nx, Ny), L=(Lx, Ly))
    grid = Grid(gc, backend)
    ccd = CCDSolver(grid, backend, bc_type="dirichlet")

    # Grid: x in [0,1], y in [-0.5, 0.5]
    # Note: Grid class generates [0,L], we remap y
    x = np.linspace(0.0, Lx, Nx + 1)
    y = np.linspace(-0.5, 0.5, Ny + 1)
    X, Y = np.meshgrid(x, y, indexing='ij')

    u_ex, v_ex, p_ex, lam = kovasznay_exact(X, Y, Re)

    # CCD derivatives of exact solution
    du_dx, d2u_dx2 = ccd_ops(u_ex, ccd, backend, axis=0)
    du_dy, d2u_dy2 = ccd_ops(u_ex, ccd, backend, axis=1)
    dv_dx, d2v_dx2 = ccd_ops(v_ex, ccd, backend, axis=0)
    dv_dy, d2v_dy2 = ccd_ops(v_ex, ccd, backend, axis=1)
    dp_dx, _ = ccd_ops(p_ex, ccd, backend, axis=0)
    dp_dy, _ = ccd_ops(p_ex, ccd, backend, axis=1)

    # NS residual: R = -u·∇u + ν∇²u - ∇p
    # For exact steady solution, R_exact = 0
    conv_u = u_ex * du_dx + v_ex * du_dy
    conv_v = u_ex * dv_dx + v_ex * dv_dy
    visc_u = nu * (d2u_dx2 + d2u_dy2)
    visc_v = nu * (d2v_dx2 + d2v_dy2)

    Rx = -conv_u + visc_u - dp_dx
    Ry = -conv_v + visc_v - dp_dy

    # Divergence residual
    div_u = du_dx + dv_dy

    # Interior-only error (exclude boundary nodes)
    margin = 2  # skip 2 boundary nodes to avoid BC contamination
    sl = (slice(margin, Nx+1-margin), slice(margin, Ny+1-margin))

    res_x = np.max(np.abs(Rx[sl]))
    res_y = np.max(np.abs(Ry[sl]))
    res_vel = max(res_x, res_y)
    res_div = np.max(np.abs(div_u[sl]))

    return res_vel, res_div, res_x, res_y


def main():
    print("\n" + "=" * 80)
    print("  【11-3b】Kovasznay Flow: NS Spatial Accuracy (MMS Residual)")
    print("=" * 80 + "\n")

    Re = 40.0
    Ns = [16, 32, 64, 128]
    results = []

    print(f"  Re={Re}")
    print(f"  {'N':>5} {'Res_vel':>12} {'order':>8} {'Res_div':>12} {'order_div':>10}")
    print("  " + "-" * 55)

    for N in Ns:
        res_vel, res_div, res_x, res_y = compute_ns_residual(N, Re=Re)
        results.append({"N": N, "res_vel": res_vel, "res_div": res_div})

        order_str = "---"
        order_div_str = "---"
        if len(results) > 1:
            r0, r1 = results[-2], results[-1]
            h0, h1 = 1.0/r0["N"], 1.0/r1["N"]
            if r0["res_vel"] > 0 and r1["res_vel"] > 0:
                s = np.log(r1["res_vel"]/r0["res_vel"]) / np.log(h1/h0)
                order_str = f"{s:.2f}"
            if r0["res_div"] > 0 and r1["res_div"] > 0:
                s = np.log(r1["res_div"]/r0["res_div"]) / np.log(h1/h0)
                order_div_str = f"{s:.2f}"

        print(f"  {N:>5} {res_vel:>12.3e} {order_str:>8} {res_div:>12.3e} {order_div_str:>10}")

    # Save LaTeX table
    with open(OUT / "table_kovasznay.tex", "w") as fp:
        fp.write("% Auto-generated by exp11_5_kovasznay.py\n")
        fp.write("\\begin{tabular}{rrcrc}\n\\toprule\n")
        fp.write("$N$ & NS 残差 $\\|R\\|_\\infty$ & 次数 & $\\|\\bnabla\\cdot\\bu\\|_\\infty$ & 次数 \\\\\n")
        fp.write("\\midrule\n")
        for i, r in enumerate(results):
            ov = "---"
            od = "---"
            if i > 0:
                r0 = results[i-1]
                h0, h1 = 1.0/r0["N"], 1.0/r["N"]
                if r0["res_vel"] > 0 and r["res_vel"] > 0:
                    ov = f"${np.log(r['res_vel']/r0['res_vel'])/np.log(h1/h0):.2f}$"
                if r0["res_div"] > 0 and r["res_div"] > 0:
                    od = f"${np.log(r['res_div']/r0['res_div'])/np.log(h1/h0):.2f}$"
            fp.write(f"{r['N']} & ${r['res_vel']:.2e}$ & {ov} & ${r['res_div']:.2e}$ & {od} \\\\\n")
        fp.write("\\bottomrule\n\\end{tabular}\n")
    print(f"\n  Saved: {OUT / 'table_kovasznay.tex'}")

    np.savez(OUT / "kovasznay_data.npz", results=results)
    print(f"  All results saved to {OUT}")


if __name__ == "__main__":
    main()
