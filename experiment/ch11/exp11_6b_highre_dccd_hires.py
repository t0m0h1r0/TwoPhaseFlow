#!/usr/bin/env python3
"""【11-4b】High-Re double shear layer: high-resolution CCD vs DCCD.

Paper ref: §11.4, Table tab:highre_dccd_hires
N=128 (1000 steps) and N=256 (4000 steps), Re=1000, T=0.5.

At high resolution, Nyquist-mode oscillations grow via nonlinear energy cascade.
DCCD filter (ε_d=0.05) should suppress checkerboard instability while
CCD (ε_d=0) may blow up.
"""

import sys, pathlib
sys.path.insert(0, str(pathlib.Path(__file__).resolve().parents[2] / "src"))

import numpy as np
from twophase.backend import Backend
from twophase.core.grid import Grid
from twophase.config import GridConfig
from twophase.ccd.ccd_solver import CCDSolver

sys.path.insert(0, str(pathlib.Path(__file__).resolve().parent.parent / "ch10"))
from exp10_10_ipc_time_accuracy import (
    SpectralPPE, SpectralHelmholtz,
    ccd_divergence, ccd_gradient, ccd_laplacian, ccd_convection,
)

sys.path.insert(0, str(pathlib.Path(__file__).resolve().parent))
from exp11_6_highre_dccd import double_shear_ic, d10_filter_2d

OUT = pathlib.Path(__file__).resolve().parent / "results" / "highre_dccd"
OUT.mkdir(parents=True, exist_ok=True)


def run_shear_layer(N, Re, T_end, dt, eps_d=0.0, label="CCD"):
    """Run double shear layer with AB2+IPC (same as exp11_6 but with progress)."""
    backend = Backend(use_gpu=False)
    nu = 1.0 / Re
    L = 2.0 * np.pi
    n_steps = int(T_end / dt)

    gc = GridConfig(ndim=2, N=(N, N), L=(L, L))
    grid = Grid(gc, backend)
    ccd = CCDSolver(grid, backend, bc_type="periodic")
    ppe_solver = SpectralPPE(N, L)
    helmholtz = SpectralHelmholtz(N, L)
    alpha_cn = dt * nu / 2.0

    X, Y = grid.meshgrid()
    u, v, p = double_shear_ic(X, Y)

    conv_u_prev = None
    conv_v_prev = None
    Ek_history = []
    blowup = False

    print_interval = max(n_steps // 10, 1)

    for step in range(n_steps):
        t = (step + 1) * dt

        conv_u, conv_v = ccd_convection(u, v, ccd, backend)
        if conv_u_prev is not None:
            ab2_u = 1.5 * conv_u - 0.5 * conv_u_prev
            ab2_v = 1.5 * conv_v - 0.5 * conv_v_prev
        else:
            ab2_u = conv_u
            ab2_v = conv_v
        conv_u_prev = conv_u.copy()
        conv_v_prev = conv_v.copy()

        visc_u = nu * ccd_laplacian(u, ccd, backend)
        visc_v = nu * ccd_laplacian(v, ccd, backend)
        dp_dx, dp_dy = ccd_gradient(p, ccd, backend)

        rhs_u = u + dt * (-ab2_u + 0.5 * visc_u - dp_dx)
        rhs_v = v + dt * (-ab2_v + 0.5 * visc_v - dp_dy)

        u_star = helmholtz.solve(rhs_u, alpha_cn)
        v_star = helmholtz.solve(rhs_v, alpha_cn)

        div_star = ccd_divergence(u_star, v_star, ccd, backend)
        delta_p = ppe_solver.solve(div_star / dt)
        ddp_dx, ddp_dy = ccd_gradient(delta_p, ccd, backend)
        u = u_star - dt * ddp_dx
        v = v_star - dt * ddp_dy
        p = p + delta_p

        if eps_d > 0:
            u = d10_filter_2d(u, alpha_f=eps_d * 4)
            v = d10_filter_2d(v, alpha_f=eps_d * 4)

        h = L / N
        Ek = 0.5 * np.sum((u[:N, :N]**2 + v[:N, :N]**2)) * h**2
        Ek_history.append(Ek)

        if np.isnan(Ek) or Ek > 1e10:
            print(f"    [{label} N={N}] BLOWUP at step {step+1}/{n_steps}, t={t:.4f}")
            blowup = True
            break

        if (step + 1) % print_interval == 0:
            print(f"    [{label} N={N}] step {step+1}/{n_steps}, t={t:.3f}, Ek={Ek:.6e}")

    return {
        "label": label, "eps_d": eps_d, "N": N,
        "n_steps": len(Ek_history),
        "Ek_final": Ek_history[-1] if Ek_history else np.nan,
        "blowup": blowup,
    }


def main():
    print("\n" + "=" * 80)
    print("  【11-4b】High-Re Double Shear Layer (N=128, 256)")
    print("=" * 80)

    Re = 1000.0
    T_end = 0.5
    configs = [
        {"N": 128, "dt": T_end / 1000},   # 1000 steps
    ]

    all_results = []

    for cfg in configs:
        N, dt = cfg["N"], cfg["dt"]
        n_steps = int(T_end / dt)
        print(f"\n--- N={N}, dt={dt:.6f}, steps={n_steps} ---")

        print(f"  Running CCD (ε_d=0)...")
        r_ccd = run_shear_layer(N, Re, T_end, dt, eps_d=0.0, label="CCD")

        print(f"  Running DCCD (ε_d=0.05)...")
        r_dccd = run_shear_layer(N, Re, T_end, dt, eps_d=0.05, label="DCCD")

        all_results.append(r_ccd)
        all_results.append(r_dccd)

        for r in [r_ccd, r_dccd]:
            status = "BLOWUP" if r["blowup"] else "STABLE"
            print(f"    {r['label']:>5} ε_d={r['eps_d']:.2f}: "
                  f"steps={r['n_steps']}, Ek={r['Ek_final']:.6e}, {status}")

    # Save LaTeX table
    with open(OUT / "table_highre_dccd_hires.tex", "w") as fp:
        fp.write("% Auto-generated by exp11_6b_highre_dccd_hires.py\n")
        for r in all_results:
            status = "発散" if r["blowup"] else "安定"
            fp.write(f"  & {r['label']} & ${r['eps_d']:.2f}$ & {r['n_steps']} "
                     f"& ${r['Ek_final']:.4e}$ & {status} \\\\\n")

    print(f"\n  Saved: {OUT / 'table_highre_dccd_hires.tex'}")


if __name__ == "__main__":
    main()
