#!/usr/bin/env python3
"""Plot-only: regenerate zalesak_N128.pdf and single_vortex_N128.pdf from saved snapshots.

Requires: results/ch10_cls_advection/{zalesak,single_vortex}_N128_snapshot.npz
  (generated by interface_advection.py with make_figure=True)
"""

import pathlib
import numpy as np
import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt

SNAP_DIR = pathlib.Path(__file__).resolve().parent.parent.parent / "results" / "ch10_cls_advection"
FIG_DIR  = pathlib.Path(__file__).resolve().parent.parent.parent / "paper" / "figures"


def plot_snapshot(name, title_initial, title_final):
    path = SNAP_DIR / f"{name}_N128_snapshot.npz"
    if not path.exists():
        print(f"  SKIP {name}: {path} not found (run interface_advection.py first)")
        return
    d = np.load(path)
    X, Y, psi0, psi_T, N = d["X"], d["Y"], d["psi0"], d["psi_T"], int(d["N"])

    fig, axes = plt.subplots(1, 2, figsize=(9, 4),
                             gridspec_kw={"wspace": 0.25})
    for ax, psi, title in [(axes[0], psi0, title_initial),
                            (axes[1], psi_T, title_final)]:
        c = ax.contourf(X, Y, psi, levels=50, cmap="RdBu_r", vmin=0, vmax=1)
        ax.contour(X, Y, psi0, levels=[0.5], colors="k", linewidths=0.8,
                   linestyles="--")
        ax.contour(X, Y, psi, levels=[0.5], colors="r", linewidths=1.2)
        ax.set_aspect("equal")
        ax.set_title(f"{title}\n$N = {N}$")
        ax.set_xlabel("$x$")
        ax.set_ylabel("$y$")

    fig.subplots_adjust(right=0.88)
    cax = fig.add_axes([0.90, 0.15, 0.02, 0.7])
    fig.colorbar(c, cax=cax, label=r"$\psi$")

    out = FIG_DIR / f"{name}_N{N}.pdf"
    fig.savefig(out, dpi=150, bbox_inches="tight")
    plt.close(fig)
    print(f"  Saved: {out}")


if __name__ == "__main__":
    print("Regenerating CLS advection snapshot figures...")
    plot_snapshot("zalesak", "Initial (t=0)", "After 1 rev (t=T)")
    plot_snapshot("single_vortex", "Initial (t=0)", "After reversal (t=T)")
    print("Done.")
