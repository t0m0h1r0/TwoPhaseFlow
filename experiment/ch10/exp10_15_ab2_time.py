#!/usr/bin/env python3
"""【10-15】AB2 time integration accuracy verification.

Paper ref: §10.3.5 (sec:verify_ab2)

Tests:
  ODE dq/dt = -q, q(0)=1, exact q(T=1) = e^{-1}.
  AB2 (Adams-Bashforth 2nd order) with forward Euler startup at n=0.
  n = 16, 32, 64, 128, 256, 512 steps.

Expected:
  O(Δt²) convergence. Initial Euler step (O(Δt)) decays via parasitic
  root |ρ₂|=0.5 and does not affect global order.
"""

import sys, pathlib
sys.path.insert(0, str(pathlib.Path(__file__).resolve().parents[2] / "src"))

import numpy as np

OUT = pathlib.Path(__file__).resolve().parent.parent.parent / "results" / "ch10_ab2_time"
OUT.mkdir(parents=True, exist_ok=True)


def ab2_ode_test():
    """AB2 on dq/dt = -q, q(0) = 1, T = 1.

    n=0: Forward Euler  q^1 = q^0 + Δt f(q^0)
    n≥1: AB2            q^{n+1} = q^n + Δt (3/2 f^n - 1/2 f^{n-1})

    f(q) = -q.
    """
    T = 1.0
    q_exact = np.exp(-T)

    n_steps_list = [16, 32, 64, 128, 256, 512]
    results = []

    print(f"\n  {'n':>6} | {'Δt':>12} | {'|q - e⁻¹|':>12} | {'order':>6}")
    print("  " + "-" * 48)

    for n_steps in n_steps_list:
        dt = T / n_steps
        q = 1.0
        f_prev = None

        for n in range(n_steps):
            f_n = -q  # f(q^n)

            if n == 0:
                # Forward Euler startup
                q_new = q + dt * f_n
            else:
                # AB2: q^{n+1} = q^n + Δt (3/2 f^n - 1/2 f^{n-1})
                q_new = q + dt * (1.5 * f_n - 0.5 * f_prev)

            f_prev = f_n
            q = q_new

        err = abs(q - q_exact)
        results.append({"n_steps": n_steps, "dt": dt, "err": err})

        # Convergence order
        order_str = "---"
        if len(results) > 1:
            r0, r1 = results[-2], results[-1]
            if r0["err"] > 1e-15 and r1["err"] > 1e-15:
                order = np.log(r1["err"] / r0["err"]) / np.log(r1["dt"] / r0["dt"])
                order_str = f"{order:.2f}"

        print(f"  {n_steps:>6} | {dt:>12.4e} | {err:>12.3e} | {order_str:>6}")

    return results


def save_latex_table(results):
    with open(OUT / "table_ab2_ode.tex", "w") as fp:
        fp.write("% Auto-generated by exp10_15_ab2_time.py\n")
        fp.write("\\begin{tabular}{rrrr}\n\\toprule\n")
        fp.write("ステップ数 $n$ & $\\Delta t$ & $|q - e^{-1}|$ & 次数 \\\\\n\\midrule\n")

        for i, r in enumerate(results):
            order_str = "---"
            if i > 0:
                r0 = results[i-1]
                if r0["err"] > 1e-15 and r["err"] > 1e-15:
                    order = np.log(r["err"] / r0["err"]) / np.log(r["dt"] / r0["dt"])
                    order_str = f"{order:.2f}"

            fp.write(f"  {r['n_steps']} & ${r['dt']:.2e}$ & ${r['err']:.2e}$ & {order_str} \\\\\n")

        fp.write("\\bottomrule\n\\end{tabular}\n")

    print(f"\n  Saved: {OUT / 'table_ab2_ode.tex'}")


def save_plot(results):
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt

    fig, ax = plt.subplots(1, 1, figsize=(6, 4.5))

    dt_arr = [r["dt"] for r in results]
    err_arr = [r["err"] for r in results]
    ax.loglog(dt_arr, err_arr, "o-", label="AB2 (Euler startup)", markersize=7)

    # Reference slopes
    dt_ref = np.array([dt_arr[0], dt_arr[-1]])
    e0 = err_arr[0]
    for order, ls, label in [(1, ":", "$O(\\Delta t)$"),
                              (2, "--", "$O(\\Delta t^2)$")]:
        ax.loglog(dt_ref, e0 * (dt_ref / dt_ref[0])**order, ls,
                  color="gray", alpha=0.5, label=label)

    ax.set_xlabel("$\\Delta t$")
    ax.set_ylabel("$|q - e^{-1}|$")
    ax.set_title("AB2 ODE convergence: $dq/dt = -q$")
    ax.legend(fontsize=8)
    ax.grid(True, which="both", alpha=0.3)

    fig.tight_layout()
    fig.savefig(OUT / "ab2_ode_convergence.png", dpi=150, bbox_inches="tight")
    plt.close(fig)
    print(f"  Saved: {OUT / 'ab2_ode_convergence.png'}")


def main():
    print("\n" + "=" * 80)
    print("  【10-15】AB2 Time Integration Accuracy")
    print("=" * 80)

    results = ab2_ode_test()
    save_latex_table(results)
    save_plot(results)

    np.savez(OUT / "ab2_ode_data.npz", results=results)
    print(f"\n  All results saved to {OUT}")


if __name__ == "__main__":
    main()
